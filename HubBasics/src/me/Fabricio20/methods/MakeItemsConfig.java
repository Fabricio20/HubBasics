	public static void make1() {
		CustomConfigs.getItemConfig().set("Item1.Enabled", true);
		CustomConfigs.getItemConfig().set("Item1.Skull", false);
		CustomConfigs.getItemConfig().set("Item1.Material", "PAPER");
		CustomConfigs.getItemConfig().set("Item1.Name", "&bThis is a paper!");
		List<String> NewLore = new ArrayList<String>();
		NewLore.add("&cThis is the &bPAPER's &cLore!");
		CustomConfigs.getItemConfig().set("Item1.Lore", NewLore);
		CustomConfigs.getItemConfig().set("Item1.Slot", 0);
		CustomConfigs.getItemConfig().set("Item1.Command", "/me I just installed HubBasics!");
		CustomConfigs.saveItemConfig();
	}
	
